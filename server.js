// Fun√ß√£o para validar n√∫mero de telefone brasileiro
function validatePhoneNumber(phone) {
    if (!phone) return false;
    // Remove espa√ßos, par√™nteses e tra√ßos
    const cleanPhone = phone.replace(/[\s\(\)\-]/g, '');
    // Verifica se tem formato brasileiro com c√≥digo do pa√≠s
    return /^55\d{10,11}$/.test(cleanPhone);
}

// Fun√ß√£o melhorada para enviar WhatsApp via CallMeBot
async function sendCallMeBotWhatsApp(message, retryCount = 0) {
    try {
        if (!CALLMEBOT_CONFIG.phone || !CALLMEBOT_CONFIG.apiKey) {
            throw new Error('CallMeBot n√£o configurado - defina CALLMEBOT_PHONE e CALLMEBOT_APIKEY');
        }

        // Validar n√∫mero de telefone
        if (!validatePhoneNumber(CALLMEBOT_CONFIG.phone)) {
            throw new Error('N√∫mero de telefone inv√°lido. Use formato: 5581999999999');
        }

        // Limitar tamanho da mensagem (CallMeBot tem limite)
        if (message.length > 1000) {
            console.warn('‚ö†Ô∏è Mensagem muito longa, truncando...');
            message = message.substring(0, 997) + '...';
        }

        const url = `${CALLMEBOT_CONFIG.baseUrl}?phone=${encodeURIComponent(CALLMEBOT_CONFIG.phone)}&text=${encodeURIComponent(message)}&apikey=${CALLMEBOT_CONFIG.apiKey}`;
        
        console.log(`üì± Tentativa ${retryCount + 1} - Enviando CallMeBot WhatsApp...`);
        
        const response = await axios.get(url, { 
            timeout: CALLMEBOT_CONFIG.timeout,
            headers: {
                'User-Agent': 'MedChat-Server/2.1.0'
            }
        });
        
        // Verificar se a resposta indica sucesso
        if (response.status === 200) {
            console.log('‚úÖ CallMeBot WhatsApp enviado com sucesso!');
            return { 
                success: true, 
                response: response.data,
                attempt: retryCount + 1,
                messageLength: message.length
            };
        } else {
            throw new Error(`Status HTTP: ${response.status}`);
        }
        
    } catch (error) {
        console.error(`‚ùå Erro CallMeBot (tentativa ${retryCount + 1}):`, error.message);
        
        // Retry logic para falhas tempor√°rias
        if (retryCount < CALLMEBOT_CONFIG.maxRetries - 1) {
            const delay = Math.pow(2, retryCount) * 1000; // Exponential backoff
            console.log(`üîÑ Tentando novamente em ${delay}ms...`);
            
            await new Promise(resolve => setTimeout(resolve, delay));
            return sendCallMeBotWhatsApp(message, retryCount + 1);
        }
        
        return { 
            success: false, 
            error: error.message,
            totalAttempts: retryCount + 1
        };
    }
}

